    public void populateRequestTable(){
        DefaultTableModel dtm=(DefaultTableModel)requestTable.getModel();
        dtm.setRowCount(0);
        for(WorkRequest request1:userAccount.getWorkQueue().getWorkRequestList()){
            RequestForActor request=(RequestForActor)request1;
            Object row[]=new Object[4];
            row[0]=request;
            row[1]=request.getReceiver();
            row[2]=request.getSender();
            row[3]=request.getStatus().equals("Sent")?"Pending":request.getStatus();
            dtm.addRow(row);
        }
    }


