    private void viewButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewButtonActionPerformed
          int selectedrow=requestTable.getSelectedRow();
          if(selectedrow<0){
              JOptionPane.showMessageDialog(null, "Please select an row first.");
              return;
          }
          RequestForActor request=(RequestForActor)requestTable.getValueAt(selectedrow, 0);
          Movie movie=request.getMovie();
          ActorViewStaffPanel panel=new ActorViewStaffPanel(card,movie,enterprise);
          card.add("ActorViewStaffPanel",panel);
          CardLayout layout=(CardLayout)card.getLayout();
          layout.next(card);
        // TODO add your handling code here:
    }//GEN-LAST:event_viewButtonActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
          int selectedrow=requestTable.getSelectedRow();
          if(selectedrow<0){
              JOptionPane.showMessageDialog(null, "Please select an row first.");
              return;
          }
          RequestForActor request=(RequestForActor)requestTable.getValueAt(selectedrow, 0);
          Movie movie=request.getMovie();
          request.setStatus("Approved");
          request.setReceiver(userAccount);
          movie.getEmployeelist().getEmployeeList().add(userAccount.getEmployee());
          userAccount.getMovielist().getMovieList().add(movie);
          populateRequestTable();
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed


