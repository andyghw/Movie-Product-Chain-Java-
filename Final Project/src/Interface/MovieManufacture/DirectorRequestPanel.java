    private void requestButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_requestButtonActionPerformed

         Movie movie=(Movie)movieCombox.getSelectedItem();
         Enterprise e=(Enterprise)sponsorCombox.getSelectedItem();
         try{
             double expectedBudget=Double.parseDouble(budgetText.getText()); 
         RequestForSponsorship workrequest=new RequestForSponsorship();
         workrequest.setSender(userAccount);
         workrequest.setStatus("Sent");
         workrequest.setMovie(movie);
         workrequest.setEnterprise(e);
         workrequest.getMovie().setBudget(expectedBudget);
         userAccount.getWorkQueue().getWorkRequestList().add(workrequest);
         for(Network network:business.getNetworkslist()){
             for(Enterprise enterprise:network.getEnterpriseDirectory().getEnterpriseslist()){
                 if(enterprise instanceof SponsorEnterprise){
                     for(Organization organization:enterprise.getOrganizationDirectory().getOrganizationList()){
                         if(organization instanceof SponsorManagerOrganization){
                             organization.getWorkQueue().getWorkRequestList().add(workrequest);
                         }
                     }
                 }
             }
         }
         JOptionPane.showMessageDialog(null, "Request successfully!");
         populateRequestTable2();
         }
         catch(Exception ee){
             JOptionPane.showMessageDialog(null, "Please input a valid expected budget.");
         }
        // TODO add your handling code here:
    }//GEN-LAST:event_requestButtonActionPerformed

