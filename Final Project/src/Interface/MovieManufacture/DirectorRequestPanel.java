    public void populateMovieCombox(){
        movieCombox.removeAllItems();
        for(Movie movie:userAccount.getMovielist().getMovieList()){
            movieCombox.addItem(movie);
        }
    }

    
    public void populateSponsorCombox(){
        sponsorCombox.removeAllItems();
        for(Network network:business.getNetworkslist()){
            for(Enterprise enterprise:network.getEnterpriseDirectory().getEnterpriseslist()){
                if(enterprise instanceof SponsorEnterprise){
                    sponsorCombox.addItem(enterprise);
                }
            }
        }
    }
    

    
    public void populateRequestTable2(){
        DefaultTableModel dtm=(DefaultTableModel)requestTable.getModel();
        dtm.setRowCount(0);
        for(WorkRequest request:userAccount.getWorkQueue().getWorkRequestList()){
            if(request instanceof RequestForSponsorship){
            RequestForSponsorship request2=(RequestForSponsorship)request;
            Object row[]=new Object[4];
            row[0]=request2;
            row[1]=request2.getReceiver();
            row[2]=request2.getStatus();
            row[3]=request2.getMovie().getBudget();
            dtm.addRow(row);
            }
        }
    }

