    private void selectStaffButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectStaffButton1ActionPerformed
        int selectedrow=requestTable.getSelectedRow();
        if(selectedrow<0){
            JOptionPane.showMessageDialog(null, "Please select an row first.");
            return;
        }
        RequestForSponsorship workRequest=(RequestForSponsorship)requestTable.getValueAt(selectedrow, 0);
        Movie movie=workRequest.getMovie();
        if(movie==null){
            JOptionPane.showMessageDialog(null, "Please select a movie first.");
            return;
        }
        SelectStaffPanel panel=new SelectStaffPanel(card,business,movie,enterprise,userAccount);
        card.add("SelectStaffPanel",panel);
        CardLayout layout=(CardLayout)card.getLayout();
        layout.next(card);

        // TODO add your handling code here:
    }//GEN-LAST:event_selectStaffButton1ActionPerformed


