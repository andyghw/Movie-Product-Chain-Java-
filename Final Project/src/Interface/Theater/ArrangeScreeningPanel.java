    public void populateMovieCombox(){
        movieCombox.removeAllItems();
        for(Movie movie:userAccount.getMovielist().getMovieList()){
            movieCombox.addItem(movie);
        }
    }
    
    public void populateSessionTable(){
        DefaultTableModel dtm=(DefaultTableModel)sessionTable.getModel();
        dtm.setRowCount(0);
        for(Session session:userAccount.getSessionlist().getSessionlist()){
            Object row[]=new Object[4];
            row[0]=session;
            row[1]=session.getDate();
            row[2]=session.getTime();
            row[3]=session.getPrice();
            dtm.addRow(row);
        }
    }

    public void populateDateCombox(){
        dateCombox.removeAllItems();
        String month=(String)monthCombox.getSelectedItem();
        if(month.equals("1")|month.equals("3")|month.equals("5")|month.equals("7")|month.equals("8")|month.equals("10")|month.equals("12")){
            for(int i=1;i<=31;i++){
                dateCombox.addItem(String.valueOf(i));
            }
        }
        if(month.equals("4")|month.equals("6")|month.equals("9")|month.equals("11")){
            for(int i=1;i<=30;i++){
                dateCombox.addItem(String.valueOf(i));
            }
        }
        if(month.equals("2")){
            for(int i=1;i<=28;i++){
                dateCombox.addItem(String.valueOf(i));
            }
        }
    }
