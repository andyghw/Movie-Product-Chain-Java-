    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(movieCombox==null){
            JOptionPane.showMessageDialog(null, "Please select a movie first.");
            return;
        }
        Movie movie=(Movie)movieCombox.getSelectedItem();
         String date=monthCombox.getSelectedItem()+"/"+dateCombox.getSelectedItem();
         if(Integer.valueOf(String.valueOf(hourSpinner.getValue()))<0|Integer.valueOf(String.valueOf(hourSpinner.getValue()))>=24|Integer.valueOf(String.valueOf(hourSpinner1.getValue()))<0|Integer.valueOf(String.valueOf(hourSpinner1.getValue()))>=24|Integer.valueOf(String.valueOf(minuteSpinner.getValue()))>=60|Integer.valueOf(String.valueOf(minuteSpinner.getValue()))<0|Integer.valueOf(String.valueOf(minuteSpinner1.getValue()))>=60|Integer.valueOf(String.valueOf(minuteSpinner.getValue()))<0){
             JOptionPane.showMessageDialog(null, "Please choose a valid time.");
             return;
         }
         String time=hourSpinner.getValue().toString()+":"+minuteSpinner.getValue().toString()+" to "+hourSpinner1.getValue().toString()+":"+minuteSpinner1.getValue().toString();
         try{
         double price=Double.valueOf(priceText.getText());
         userAccount.getSessionlist().createSession(movie, date, time, price);
         JOptionPane.showMessageDialog(null, "Arrange successfully!");
         populateSessionTable();
         }
         catch(Exception e){
             JOptionPane.showMessageDialog(null, "Please input a valid price.");
         }
         
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

