     public void populateActorInfo(){
        nameText.setText(employee.getName());
        ratingText.setText(String.valueOf(employee.getRating()));
        DefaultTableModel dtm=(DefaultTableModel)movieTable.getModel();
        dtm.setRowCount(0);
        for(Network network:business.getNetworkslist()){
            for(Enterprise enterprise:network.getEnterpriseDirectory().getEnterpriseslist()){
                if(enterprise instanceof MovieManufactureEnterprise){
                    for(Organization organization:enterprise.getOrganizationDirectory().getOrganizationList()){
                        if(organization instanceof ActorOrganization){
                            for(UserAccount userAccount:organization.getUserAccountDirectory().getUserAccountList()){
                                for(Movie movie:userAccount.getMovielist().getMovieList()){
                                Object row[]=new Object[3];
                                row[0]=movie;
                                row[1]=movie.getRating();
                                for(Employee e:movie.getEmployeelist().getEmployeeList()){
                                    if(e.equals(employee)){
                                        row[2]=e.getRating();
                                    }
                                }
                                dtm.addRow(row);
                                }
                            }
                        }
                    }
                }
            }
        }
    }

