    public void populateSeatTable(){
        DefaultTableModel dtm=(DefaultTableModel)seatTable.getModel();
        dtm.setRowCount(0);
        for(Seat seat:session.getSeatlist().getSeatlist()){
            Object row[]=new Object[2];
            row[0]=seat;
            row[1]=seat.getStatus();
            dtm.addRow(row);
        }
    }
    
    

