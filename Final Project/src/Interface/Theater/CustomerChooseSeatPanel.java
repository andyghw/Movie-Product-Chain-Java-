    private void bookButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bookButtonActionPerformed
          int selectedrow=seatTable.getSelectedRow();
          if(selectedrow<0){
              JOptionPane.showMessageDialog(null, "Please select an row first.");
              return;
          }
          Seat seat=(Seat)seatTable.getValueAt(selectedrow, 0);
          if(seat.getStatus().equals("unavailable")){
              JOptionPane.showMessageDialog(null, "This seat has already been booked.");
              return;
          }
          seat.setStatus("unavailable");
          seatlist.getSeatlist().add(seat);
          populateSeatTable();
          
        // TODO add your handling code here:
    }//GEN-LAST:event_bookButtonActionPerformed

