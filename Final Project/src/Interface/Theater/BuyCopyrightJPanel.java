    public void populateMovieTable2(){
        DefaultTableModel dtm=(DefaultTableModel)movieTable.getModel();
        dtm.setRowCount(0);
        for(Network network:business.getNetworkslist()){
            for(Enterprise enterprise:network.getEnterpriseDirectory().getEnterpriseslist()){
                for(Organization organization:enterprise.getOrganizationDirectory().getOrganizationList()){
                    for(UserAccount account:organization.getUserAccountDirectory().getUserAccountList()){
                            for(Movie movie:account.getMovielist().getMovieList()){
                                Object row[]=new Object[3];
                                if(account.getRole() instanceof DirectorRole){
                                    row[0]=movie;
                                    dtm.addRow(row);     
                            }

                            
                }
            }
        }
    }
        }
    }



