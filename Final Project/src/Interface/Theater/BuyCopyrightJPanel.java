    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         int selectedrow=movieTable.getSelectedRow();
         if(selectedrow<0){
             JOptionPane.showMessageDialog(null, "Please select an row first.");
             return;
         }
         try{
         double investment=Double.valueOf(investText.getText());
         Movie movie=(Movie)movieTable.getValueAt(selectedrow, 0);
         RequestForCopyRight workrequest=new RequestForCopyRight();
         workrequest.setSender(userAccount);
         workrequest.setStatus("Sent");
         workrequest.setMovie(movie);
         workrequest.setInvestment(investment);
         userAccount.getWorkQueue().getWorkRequestList().add(workrequest);
         for(Network network:business.getNetworkslist()){
             for(Enterprise enterprise:network.getEnterpriseDirectory().getEnterpriseslist()){
                 if(enterprise instanceof MovieManufactureEnterprise){
                     for(Organization organization:enterprise.getOrganizationDirectory().getOrganizationList()){
                         if(organization instanceof DirectorOrganization){
                             organization.getWorkQueue().getWorkRequestList().add(workrequest);
                         }
                     }
                 }
             }
         JOptionPane.showMessageDialog(null, "Request has been sent.");
         }
         }
         catch(Exception e){
             JOptionPane.showMessageDialog(null, "Please input a valid investment.");
             
         }
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed


